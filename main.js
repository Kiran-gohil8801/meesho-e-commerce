    function renderProducts(product){

        const imagesContainer = document.querySelector(".images");
        imagesContainer.innerHTML = "";
        product.forEach((el) => {
        var anchor = document.createElement("a");
        anchor.style.textDecoration = "none";
        anchor.style.color = "inherit";

        var div = document.createElement("div");
        div.style.height = "480px";

        var img = document.createElement("img");
        img.src = el.img;

        var title = document.createElement("h6");
        title.innerHTML = el.Title;

        var price = document.createElement("h1");
        price.innerHTML = `₹${el.price}`;
        price.style.fontWeight = "700";

        var qty = document.createElement("h6");
        qty.innerHTML = `Quantity: ${el.qty}`;

        var delivery = document.createElement("h6");
        delivery.innerHTML = el.delivery;

        var reveiw=document.createElement("button");
        reveiw.innerHTML=el.Review;
        reveiw.style.background="green";
        reveiw.style.color="white";
        reveiw.style.borderRadius="15px";
        reveiw.style.textAlign="center";
        reveiw.style.border="none";
        reveiw.style.marginTop="5px";

        div.append(img, title, price, qty, delivery,reveiw);
        anchor.append(div);

        var images = document.querySelector(".images");
        images.append(anchor);

        anchor.addEventListener("click", (e) => {
            e.preventDefault();

            localStorage.setItem(
                "value",
                JSON.stringify({
                    id: el.id,
                    img: el.img,
                    Title: el.Title,
                    price: el.price,
                    qty: el.qty,
                    delivery: el.delivery,
                    reveiw:el.Reveiw
                })
            );

            window.location.href = "details.html";
        });
    })}
    renderProducts(product)

    document.getElementById("sort").addEventListener("change", function () {
        const value = this.value;
        let sortedData = [...product];
        if (value === "price-asc") {
            sortedData.sort((a, b) => a.price - b.price)
        } else if (value === "price-dec") {
            sortedData.sort((a, b) => b.price - a.price)
        } else if (value === "name-asc") {
            sortedData.sort((a, b) => a.Title.localeCompare(b.Title))
        } else if (value === "name-dec") {
            sortedData.sort((a, b) => b.Title.localeCompare(a.Title))
        }

        renderProducts(sortedData);
    })

